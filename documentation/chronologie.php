<h2>Chronologie</h2> 
<p>&nbsp;</p> 
<p>Quand vous avez plac&eacute; plusieurs personnes dans un arbre, vous pouvez afficher la chronologie qui leur correspond.<br /> 
  Il suffit daller dans le menu &laquo;&nbsp;Mode&nbsp;&raquo; et de  demander le choix &laquo;&nbsp;Chronologie &raquo;.</p> 
<h4><img src="images/chrono01.jpg" alt="" width="787" height="359" class="imageBord1pt" /></h4> 
<p>La chronologie n'affiche pas toutes les personnes. Il faut que la date de naissance de la personne soit renseign&eacute;e.<br /> 
  S'il n'y a pas de date de d&eacute;c&egrave;s, on distingue deux cas :</p> 
<ul> 
  <li>si la personne &agrave; moins de 100 ans, elle est consid&eacute;r&eacute;e comme vivante et elle est donc affich&eacute;e ;</li> 
  <li>si la personne a plus de 100 ans, elle n'est pas affich&eacute;e.</li> 
</ul> 
<p>La chronologie s'affiche en deux parties. La partie haute avec l'axe du temps, les rep&egrave;res de dates et les personnes.<br /> 
  Quand plusieurs personnes sont concern&eacute;es par une date, un petit triangle noir est plac&eacute; sur le trait vertical de la date pour l'indiquer. Voyez que Marie STITU et Donna TEUR sont concern&eacute;es par la date rep&eacute;r&eacute;e 4 (le 15/11/1944).</p> 
<p>Les &eacute;v&egrave;nements qui sont pris en compte sont :</p> 
<ul> 
  <li>la naissance et le d&eacute;c&egrave;s de chaque personne ;</li> 
  <li>le mariage (date de mariage et date du contrat de mariage) si les deux conjoints sont pr&eacute;sents dans la chronologie.</li> 
</ul> 
