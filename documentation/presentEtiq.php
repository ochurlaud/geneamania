<p>Vous pouvez choisir les marges entre le texte et la bordure de l'&eacute;tiquette. Les valeurs sont exprim&eacute;es en pixels.</p>
<hr />
<h4><a id="A5"></a>Onglet Couleur fond <a href="#haut"><img src="images/debut.gif" alt="" width="16" height="16" class="imageSansBord" /></a></h4>
<p><img src="images/editEtiq05.png" width="375" height="300" alt="" /></p>
<p>Vous choisissez la couleur du fond de l'&eacute;tiquette.</p>
<hr />
<h4><a id="A6"></a>Onglet Couleur bordure <a href="#haut"><img src="images/debut.gif" alt="" width="16" height="16" class="imageSansBord" /></a></h4>
<p><img src="images/editEtiq06.png" width="375" height="300" alt="" /></p>
<p>Vous choisissez la couleur de la bordure de l'&eacute;tiquette.</p>
<hr />
<h4><a id="A7"></a>Onglet Forme bordure  <a href="#haut"><img src="images/debut.gif" alt="" width="16" height="16" class="imageSansBord" /></a></h4>
<p><img src="images/editEtiq07.png" width="375" height="300" alt="" /> </p>
<p>Vous pouvez choisir une des quatre formes &agrave; appliquer aux angles de l'&eacute;tiquette. Vous pouvez saisir la largeur et la hauteur de la forme de l'angle. Enfin, un menu d&eacute;roulant permet de choisir l'&eacute;paisseur de la bordure de l'&eacute;tiquette. </p>
