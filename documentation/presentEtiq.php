<h2><a id="haut"></a>Pr&eacute;sentation d'une &eacute;tiquette de texte </h2>
<p><a href="#A1">Boutons</a> - <a href="#A4">Marges</a> - <a href="#A5">Couleur fond</a> - <a href="#A6">Couleur bordure</a> - <a href="#A7">Forme bordure</a> </p>
<p>Pour changer la pr&eacute;sentation d'une &eacute;tiquette, il faut la s&eacute;lectionner. Ne s&eacute;lectionner pas plusieurs &eacute;tiquettes, il n'est pas pr&eacute;vu de modifier plusieurs &eacute;tiquettes en m&ecirc;me temps.</p>
<p>Quand vous avez s&eacute;lectionn&eacute; l'&eacute;tiquette, cliquez sur le bouton <img src="images/editEtiq.png" width="35" height="35" alt="" />.</p>
<p>G&eacute;n&eacute;Graphe ouvre une fen&ecirc;tre : </p>
<p><img src="images/editEtiq01.png" width="375" height="300" alt="" /></p>
<p>Les onglets permettent de choisir diff&eacute;rentes possibilit&eacute;s de modification de l'&eacute;tiquette. </p>
<hr />
<h4><a id="A1"></a>Boutons <a href="#haut"><img src="images/debut.gif" alt="" width="16" height="16" class="imageSansBord" /></a></h4>
<p>Le bouton <strong class="bouton">Valider</strong> permet d'appliquer &agrave; l'&eacute;tiquette toutes les modifications demand&eacute;es dans tous les onglets et ferme la fen&ecirc;tre.</p>
<p>Le bouton <strong class="bouton">Appliquer</strong> modifie l'&eacute;tiquette avec toutes les modifications demand&eacute;es dans tous les onglets. Cela permet de visualiser l'effet de ce que vous avez demand&eacute;. Vous pouvez continuer &agrave; modifier des valeurs dans cette fen&ecirc;tre. Si vous cliquez sur <span class="bouton">Abandon</span>, l'&eacute;tiquette retrouve son &eacute;tat avant l'ouverture de cette fen&ecirc;tre. Si vous cliquez sur <span class="bouton">Valider</span>, l'&eacute;tiquette sera modifi&eacute;e avec toutes les valeurs choisies. </p>
<p>Le bouton <strong class="bouton">Abandon</strong> permet de fermer la fen&ecirc;tre sans qu'aucune modification ne soit appliqu&eacute;e &agrave; l'&eacute;tiquette. Si vous aviez cliqu&eacute; sur <span class="bouton">Appliquer</span>, ces modifications sont annul&eacute;es. L'&eacute;tiquette retrouve l'&eacute;tat qu'elle avait avant d'appeler cette fen&ecirc;tre.</p>
<hr />
